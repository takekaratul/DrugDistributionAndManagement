    private void btnApproveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnApproveActionPerformed

        int selectedRow = requestForLicenceJTable.getSelectedRow();
        if (selectedRow < 0) {
            JOptionPane.showMessageDialog(null, "Select Row to Approve licence");
            return;
        }

        WorkRequest workRequest = (WorkRequest) requestForLicenceJTable.getValueAt(selectedRow, 0);

        if (workRequest.getStatus().equals(WorkRequest.Done)) {
            JOptionPane.showMessageDialog(null, "Licence Already Approved");
            return;
        }

        ((ForLicenceByManuWorkRequest) workRequest).getManufacturerEnterprise().setLicence(Enterprise.LicApproved);
        workRequest.setStatus(WorkRequest.Done);
        workRequest.setReceiver(userAccount);
        workRequest.setReceivingDate(lblDate.getText());
        Refresh();
        // TODO add your handling code here:
    }//GEN-LAST:event_btnApproveActionPerformed

    private void btnDetailsActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDetailsActionPerformed
        // TODO add your handling code here:

       int selectedRow = requestForDrugApproval.getSelectedRow();
        if (selectedRow < 0) {
            JOptionPane.showMessageDialog(null, "Select Row to View Details");
            return;
        }

        WorkRequest workRequest = (WorkRequest) requestForDrugApproval.getValueAt(selectedRow, 0);

        if (workRequest.getStatus().equals(WorkRequest.Done)) {
            JOptionPane.showMessageDialog(null, "Drug Already Approved");
            return;
        }
        
        ApproveDrugOfManufacturer panel = new ApproveDrugOfManufacturer(userProcessContainer, workRequest, userAccount);
        userProcessContainer.add("viewDetails", panel);
        CardLayout cardLayout = (CardLayout) userProcessContainer.getLayout();
        cardLayout.next(userProcessContainer);
        
        


        
        
    }//GEN-LAST:event_btnDetailsActionPerformed


