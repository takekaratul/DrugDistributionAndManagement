    public void Refresh() {
        int rowCount = requestForLicenceJTable.getRowCount();

        for (int i = rowCount - 1; i >= 0; i--) {
            ((DefaultTableModel) requestForLicenceJTable.getModel()).removeRow(i);
        }

        for (WorkRequest wr : fDAEnterprise.getFdaOrganization().getWorkQueue().getWorkRequestQueue()) {

            if(wr instanceof ForLicenceByManuWorkRequest)
            {
                Object row[] = new Object[6];
                row[0] = (ForLicenceByManuWorkRequest) wr;
                row[1] = wr.getSender();
                row[2] = wr.getReceiver();
                row[3] = wr.getStatus();
                row[4] = wr.getSendingDate();
                row[5] = wr.getReceivingDate();

                ((DefaultTableModel) requestForLicenceJTable.getModel()).addRow(row);
            }
            
            else if(wr instanceof DrugApprovalFromManuToFDA)
            {
                Object row[] = new Object[6];
                row[0] = (DrugApprovalFromManuToFDA) wr;
                row[1] = ((DrugApprovalFromManuToFDA) wr).getDrug();
                row[2] = wr.getStatus();
                row[3] = wr.getStatus();
                row[4] = wr.getSendingDate();
                row[5] = wr.getReceivingDate();

                ((DefaultTableModel) requestForDrugApproval.getModel()).addRow(row);
            }
        }
        
        
    }

