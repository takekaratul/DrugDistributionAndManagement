    private void AddToDirectoryActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AddToDirectoryActionPerformed
        // TODO add your handling code here:
         int selectedRow = tblAddDrug.getSelectedRow();
        if (selectedRow < 0) {
            JOptionPane.showMessageDialog(null, "select row");
            return;
        }

        String d = (String) tblAddDrug.getValueAt(selectedRow, 0);
        AddDrugToYourDirectory wr = (AddDrugToYourDirectory) tblAddDrug.getValueAt(selectedRow, 1);
        hospitalEnterprise.getAllApprovedDrugs().add(d);
        wr.setStatus(WorkRequest.Done);
        
        
    }//GEN-LAST:event_AddToDirectoryActionPerformed

    private void btnApplyLicenseActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnApplyLicenseActionPerformed
        // TODO add your handling code here:
        
        ForLicenceByHosWorkRequest work = new ForLicenceByHosWorkRequest();
        work.setHospitalEnterprise(hospitalEnterprise);
        work.setSender(account);
        work.setSendingDate(lblDate.getText());
        work.setStatus(WorkRequest.UnderPro);

        EcoSystem ecoSystem= EcoSystem.getInstance();
        for(Network n:ecoSystem.getNetworkList()){
            for (Enterprise e : n.getEnterprisedirectory().enterpriseDirectory) {
                if ((e.getEnterpriseType().equals(Enterprise.EnterpriseType.License))) {
                    ((LicenseEnterprise) e).getWorkQueue().getWorkRequestQueue().add(work);
                    hospitalEnterprise.setLicence(Enterprise.LicUnderProcess);
                    btnApplyLicense.setEnabled(false);
                }

            }

        }
    }//GEN-LAST:event_btnApplyLicenseActionPerformed

    private void btnCreateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCreateActionPerformed
        // TODO add your handling code here:
        
        if (txtFirstName.getText().equals("") || txtLastName.getText().equals("") || txtUsername.getText().equals("") || passPassword.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Some Field is Empty");
            return;
        }
        
        if(!(txtFirstName.getText().trim().matches("^[A-Za-z]+$")))
        {
            JOptionPane.showMessageDialog(null, "Please enter name in proper format");
            return;
        }
        
        if(!(txtLastName.getText().trim().matches("^[A-Za-z]+$")))
        {
            JOptionPane.showMessageDialog(null, "Please enter last name in proper format");
            return;
        }
        
        if(!(txtUsername.getText().trim().matches("^[A-Za-z]+$")))
        {
            JOptionPane.showMessageDialog(null, "Please enter user name in proper format");
            return;
        }

        if (comboBoxHospitalRole.getSelectedItem().toString().equals("Dispense Manager Organization")) {
            Employee dm = hospitalEnterprise.getDispenserManagerOrganization().getEmployeeDirectory().createEmployee(txtFirstName.getText(), txtLastName.getText());
            
            UserAccount user = hospitalEnterprise.getDispenserManagerOrganization().getUserAccountDirectory().createUserAccount(txtUsername.getText(), passPassword.getText(), dm, new DispenserManagerRole());
            JOptionPane.showMessageDialog(null, "Dispenser Manager Created Successfull");
        }

        if (comboBoxHospitalRole.getSelectedItem().toString().equals("Doctor Organization")) {
            Employee d = hospitalEnterprise.getDoctorOrganization().getEmployeeDirectory().createEmployee(txtFirstName.getText(), txtLastName.getText());
            
            UserAccount user = hospitalEnterprise.getDoctorOrganization().getUserAccountDirectory().createUserAccount(txtUsername.getText(), passPassword.getText(), d, new DoctorRole());
            JOptionPane.showMessageDialog(null, "Doctor Created Successfull");
        }
        
        

        txtFirstName.setText("");
        txtLastName.setText("");
        passPassword.setText("");
        txtUsername.setText("");
    }//GEN-LAST:event_btnCreateActionPerformed


