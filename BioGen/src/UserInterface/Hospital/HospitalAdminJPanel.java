    public void Refresh() {
        if (hospitalEnterprise.getLicence().equals(Enterprise.LicNotIssued) || hospitalEnterprise.getLicence().equals(Enterprise.LicUnderProcess)) {
            btnCreate.setEnabled(false);
            txtFirstName.setEditable(false);
            txtLastName.setEditable(false);
            passPassword.setEditable(false);
            txtUsername.setEditable(false);
            comboBoxHospitalRole.setEnabled(false);
        }
        if (hospitalEnterprise.getLicence().equals(Enterprise.LicNotIssued)) {
            btnApplyLicense.setEnabled(true);
        }
        if (hospitalEnterprise.getLicence().equals(Enterprise.LicApproved)) {
            btnApplyLicense.setEnabled(false);
        }
        
        
        for(WorkRequest wr : hospitalEnterprise.getWorkQueue().getWorkRequestQueue())
        {
            AddDrugToYourDirectory w=(AddDrugToYourDirectory)wr;
            if(!(w.getStatus().equals(WorkRequest.Done)))
                        {
                            
                                Object row[] = new Object[2];
                                row[0] = w.getDrugName();
                                row[1]=w;

                                ((DefaultTableModel) tblAddDrug.getModel()).addRow(row);
                            
                        }
        }
    }

