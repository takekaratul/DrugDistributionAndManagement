    public OrderDetailsJPanel(JPanel userProcessContainer, Order order) {
        initComponents();
        this.order=order;
        this.userProcessContainer=userProcessContainer;
        lblOrderID.setText(order.getOrderID());
        int rowCount=tblOrderItem.getRowCount();
        DefaultTableModel dtm=(DefaultTableModel)tblOrderItem.getModel();
        for(int i=rowCount-1;i>=0;i--){
            
            dtm.removeRow(i);
        }
        for (OrderItem orderItem : order.getOrderItemList()) {
            Object row[] = new Object[2];
            row[0] = orderItem;
            row[1] = orderItem.getQuantity();

            dtm.addRow(row);
        }
    }

