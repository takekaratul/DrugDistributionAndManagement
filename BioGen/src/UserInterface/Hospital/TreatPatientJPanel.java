    private void btnTreatActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTreatActionPerformed
        
        
        if(!(txtResponse.getText().trim().matches("(?<!\\S)\\d(?![^\\s.,?!])")))
        {
            JOptionPane.showMessageDialog(null, "Please enter response from 1-10");
            return;
        }
        
        if(!(txtSideEffect.getText().trim().matches("(?<!\\S)\\d(?![^\\s.,?!])")))
        {
            JOptionPane.showMessageDialog(null, "Please enter dosage from 1-10");
            return;
        }
        
        Treatment treatment=treatmentHistory.addTreatment();
        treatment.setPatient(patient);
        Drug selectedDrug=(Drug)comboBoxDrug.getSelectedItem();
        treatment.setDrug(selectedDrug);
        treatment.setDosage((int) comboBoxDosage.getSelectedItem());
        treatment.setResponse(Integer.parseInt(txtResponse.getText()));
        treatment.setSideEffects(Integer.parseInt(txtSideEffect.getText()));
        
        refresh();
        
        JOptionPane.showMessageDialog(null, "Treamtment saved successfully");
        

// TODO add your handling code here:
    }//GEN-LAST:event_btnTreatActionPerformed

