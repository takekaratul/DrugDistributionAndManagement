    public void refresh(){
        comboBoxDosage.removeAllItems();
       comboBoxDrug.removeAllItems();
       
           comboBoxDosage.addItem(1);
           comboBoxDosage.addItem(2);
           comboBoxDosage.addItem(3);
           comboBoxDosage.addItem(5);
           comboBoxDosage.addItem(7);
           comboBoxDosage.addItem(10);
       
       
       for(WorkRequest wr1:hospitalEnterprise.getDoctorOrganization().getWorkQueue().getWorkRequestQueue())
       {
           if(wr1 instanceof ForClinicalFromManuToHosp)
           {
            ForClinicalFromManuToHosp wr = (ForClinicalFromManuToHosp)wr1;
            if(!(wr.getStatus().equals("Done")))
            {
                comboBoxDrug.addItem(wr.getDrug());
            }
           }
       }
       
       txtResponse.setText("");
       txtSideEffect.setText("");
    }
    

