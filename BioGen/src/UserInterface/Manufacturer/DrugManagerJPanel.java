    private void btnAssignToMeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAssignToMeActionPerformed

        int selectedRow = requestsReceivedJTable.getSelectedRow();
        if (selectedRow < 0) {
            JOptionPane.showMessageDialog(null, "select row");
            return;
        }

        WorkRequest wr1 = (WorkRequest) requestsReceivedJTable.getValueAt(selectedRow, 0);

        if (((ToDrugManagerWorkRequest) wr1).getStatus().equals(WorkRequest.Done)) {
            JOptionPane.showMessageDialog(null, "Already Assigned");
            return;
        }

        wr1.setStatus(WorkRequest.Done);
        wr1.setReceiver(userAccount);
        wr1.setReceivingDate(lblDate.getText());

        Refresh();
        // TODO add your handling code here:
    }//GEN-LAST:event_btnAssignToMeActionPerformed

    private void btnViewDetailsARP1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnViewDetailsARP1ActionPerformed
        // TODO add your handling code here:
        int selectedRow = tblDrugsPendingApproval.getSelectedRow();
        if (selectedRow < 0) {
            JOptionPane.showMessageDialog(null, "Select Row to view details");
            return;
        }
        else
        {
            Drug  d = (Drug) tblDrugsPendingApproval.getValueAt(selectedRow, 0);

            
            int stage = 3;
            Boolean flagfda = (Boolean) tblDrugsPendingApproval.getValueAt(selectedRow, 3);
            if(flagfda==false)
            {
            Boolean flagHospital = (Boolean) tblDrugsPendingApproval.getValueAt(selectedRow, 2);
                if( flagHospital==false)
                {
                    Boolean flagPreclinical = (Boolean) tblDrugsPendingApproval.getValueAt(selectedRow, 1);
                    stage = 1;
                    if(flagPreclinical == false)
                    {
                        stage = 0;
                    }
                }
            }
            DrugApprovalStatusJPanel panel = new DrugApprovalStatusJPanel(userProcessContainer,manufacturerEnterprise,userAccount,d,stage, ecoSystem);
            userProcessContainer.add("Create Un approved Drug", panel);
            CardLayout cardLayout = (CardLayout) userProcessContainer.getLayout();
            cardLayout.next(userProcessContainer);
        }
    }//GEN-LAST:event_btnViewDetailsARP1ActionPerformed

