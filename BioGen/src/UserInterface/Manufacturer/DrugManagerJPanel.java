    public void Refresh() {
        
        ((DefaultTableModel) DrugDirectoryJTable.getModel()).setRowCount(0);
        ((DefaultTableModel) requestsReceivedJTable.getModel()).setRowCount(0);

        for (Drug drug : manufacturerEnterprise.getDrugDirectory().getDrugDirectory()) {
            Object row[] = new Object[5];
            row[0] = drug.getDrugId();
            row[1] = drug.getDrugName();
            row[2] = drug.getExpiryDate();
           // row[2] = drug.getManufacturerName();
            row[3] = drug.getNoOfUnits();
            row[4] = drug.getUnitPrice();

            ((DefaultTableModel) DrugDirectoryJTable.getModel()).addRow(row);
        }

        for (WorkRequest wr : manufacturerEnterprise.getDrugManagerOrganization().getWorkQueue().getWorkRequestQueue()) {

            if(wr instanceof ToDrugManagerWorkRequest)
            {
            Object row[] = new Object[7];
            row[0] = (ToDrugManagerWorkRequest) wr;
            row[1] = wr.getSender();
            row[2] = wr.getReceiver();
            row[3] = wr.getStatus();
            row[4] = (((ToDrugManagerWorkRequest) wr).getManufacturerEnterprise());
            row[5] = wr.getSendingDate();
            row[6] = wr.getReceivingDate();

            ((DefaultTableModel) requestsReceivedJTable.getModel()).addRow(row);
            }
        }
        
    }
    

