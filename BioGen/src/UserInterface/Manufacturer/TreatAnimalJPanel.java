    public void refresh(){
       cmbxAnimals.removeAllItems();
       for(Animal animal:manufacturerEnterprise.getPreClinicalTesterOrgnization().getAnimalDirectory().getAnimalDirectory()){
           cmbxAnimals.addItem(animal);
       }
       
       for(WorkRequest wr1:manufacturerEnterprise.getPreClinicalTesterOrgnization().getWorkQueue().getWorkRequestQueue())
       {
           if(wr1 instanceof ForTestingByManuToPreclinical)
           {
           ForTestingByManuToPreclinical wr = (ForTestingByManuToPreclinical)wr1;
           if(!(wr.getStatus().equals("Done")))
           {
               cmbxDrug.addItem(wr.getDrug());
           }
           }
       }
      
       
       
           
       
       
       txtResponse.setText("");
       txtSideEffect.setText("");
       txtAge.setText("");
       txtDosage.setText("");
    }
    

