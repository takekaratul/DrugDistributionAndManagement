    private void btnTreatActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTreatActionPerformed
        
        
        
        AnimalTreatment treatment=animalTreatmentHistory.addAnimalTreatment();
        treatment.setAnimal((Animal)cmbxAnimals.getSelectedItem());
        Drug selectedDrug=(Drug)cmbxDrug.getSelectedItem();
        treatment.setDrug(selectedDrug);
        treatment.setDosage(txtDosage.getText());
        treatment.setResponse(Integer.parseInt(txtResponse.getText()));
        treatment.setSideEffect(Integer.parseInt(txtSideEffect.getText()));
        
        for(WorkRequest wr:manufacturerEnterprise.getPreClinicalTesterOrgnization().getWorkQueue().getWorkRequestQueue())
        {
            ForTestingByManuToPreclinical forTestingByManuToPreclinical = (ForTestingByManuToPreclinical)wr;
            if(forTestingByManuToPreclinical.getDrug().equals(selectedDrug))
                    {
                        forTestingByManuToPreclinical.setStatus(WorkRequest.Done);
                        forTestingByManuToPreclinical.setReceiver(userAccount);
                        forTestingByManuToPreclinical.setReceivingDate(lblDate.getText());
                        selectedDrug.setIsApprovedByPreClinical(true);
                        
                    }
        }
        
        refresh();
        
         JOptionPane.showMessageDialog(null, "Animal treatment is added successfully!!");

// TODO add your handling code here:
    }//GEN-LAST:event_btnTreatActionPerformed

