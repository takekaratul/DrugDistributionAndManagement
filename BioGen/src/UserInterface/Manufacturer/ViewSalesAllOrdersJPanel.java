    private void btnAssignToMeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAssignToMeActionPerformed

        int selectedRow = ordersReceivedJTable.getSelectedRow();
        if (selectedRow < 0) {
            JOptionPane.showMessageDialog(null, "select row");
            return;
        }

        WorkRequest wr1 = (WorkRequest) ordersReceivedJTable.getValueAt(selectedRow, 0);

        if (((ToSalesWorkRequest) wr1).getStatus().equals(WorkRequest.Recieve) || ((ToSalesWorkRequest) wr1).getStatus().equals(WorkRequest.Done)) {
            JOptionPane.showMessageDialog(null, "Already Assigned");
            return;
        }

        wr1.setStatus(WorkRequest.Recieve);
        wr1.setReceiver(userAccount);
        wr1.setReceivingDate(lblDate.getText());

        WarehouseEnterprise whereHouseEnterprise = ((ToSalesWorkRequest) wr1).getWareHouseEnterprise();
        for (WorkRequest wr : whereHouseEnterprise.getWarehouseManagerOrganization().getWorkQueue().getWorkRequestQueue()) {
            if(wr instanceof FromWareHouseWorkRequest) {
            if ((((FromWareHouseWorkRequest) wr).getOrder().getOrderID()).equals(((ToSalesWorkRequest) wr1).getOrder().getOrderID())) {
                wr.setReceiver(userAccount);
                wr.setStatus(WorkRequest.Sent);
                wr.setReceivingDate(lblDate.getText());
            }
            }
        }

        Refresh();
        // TODO add your handling code here:
    }//GEN-LAST:event_btnAssignToMeActionPerformed

