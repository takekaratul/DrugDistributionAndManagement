    public void Refresh() {

        int rowCount = ordersReceivedJTable.getRowCount();
        for (int i = rowCount - 1; i >= 0; i--) {
            ((DefaultTableModel) ordersReceivedJTable.getModel()).removeRow(i);
        }

        int rowCount1 = ordersPlacedJTable.getRowCount();

        for (int i = rowCount1 - 1; i >= 0; i--) {
            ((DefaultTableModel) ordersPlacedJTable.getModel()).removeRow(i);
        }

        for (WorkRequest wr : manufacturerEnterprise.getSalesManagerOrganization().getWorkQueue().getWorkRequestQueue()) {

            if(wr instanceof FromSalesWorkRequest)
            {
            Object row[] = new Object[8];
            row[0] = (FromSalesWorkRequest) wr;
            row[1] = ((FromSalesWorkRequest) wr).getBatch();
            row[2] = wr.getSender();
            row[3] = wr.getReceiver();
            row[4] = wr.getStatus();
            row[5] = (((FromSalesWorkRequest) wr).getWareHouseEnterprise());
            row[6] = wr.getSendingDate();
            row[7] = wr.getReceivingDate();

            ((DefaultTableModel) ordersPlacedJTable.getModel()).addRow(row);
            }
        }

        for (WorkRequest wr : (manufacturerEnterprise.getSalesManagerOrganization().getWorkQueue().getWorkRequestQueue())) {
            if(wr instanceof ToSalesWorkRequest )
            {
            Object row[] = new Object[7];
            row[0] = (ToSalesWorkRequest) wr;
            row[1] = wr.getSender();
            row[2] = wr.getReceiver();
            row[3] = wr.getStatus();
            row[4] = ((ToSalesWorkRequest) wr).getWareHouseEnterprise();
            row[5] = wr.getSendingDate();
            row[6] = wr.getReceivingDate();

            ((DefaultTableModel) ordersReceivedJTable.getModel()).addRow(row);
            }
        }
    }
    
