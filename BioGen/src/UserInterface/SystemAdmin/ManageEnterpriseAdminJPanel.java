    private void btnSubmitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSubmitActionPerformed
        
        Network networkSelected = null;
        if (txtFirstName.getText().equals("") || txtLastName.getText().equals("") || txtUserName.getText().equals("") || txtPassword.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Some Field is Empty");
            return;
        }

        for(Network n:system.getNetworkList())
        {
            if(n.getName().equals(cmbxNetwork.getSelectedItem().toString()))
                networkSelected = n;
        }
        
        for (Enterprise enterprise : networkSelected.getEnterprisedirectory().getEnterpriseDirectory()) {
            for (UserAccount userAccount : enterprise.getUserAccountDirectory().getUserAccountList()) {
                if (txtUserName.getText().equals(userAccount.getUsername())) {
                    JOptionPane.showMessageDialog(null, "UserName already Exist");
                    return;
                }
            }

            if (enterprise.getEnterpriseType().equals(Enterprise.EnterpriseType.FDA)) {
                FDAOrganization o = ((FDAEnterprise) enterprise).getFdaOrganization();

                for (UserAccount userAccount : o.getUserAccountDirectory().getUserAccountList()) {
                    if (txtUserName.getText().equals(userAccount.getUsername())) {
                        JOptionPane.showMessageDialog(null, "UserName already Exist");
                        return;
                    }

                }
            }

            if (enterprise.getEnterpriseType().equals(Enterprise.EnterpriseType.Manufacturer)) {
                SalesManagerOrganization o = ((ManufacturerEnterprise) enterprise).getSalesManagerOrganization();

                for (UserAccount userAccount : o.getUserAccountDirectory().getUserAccountList()) {
                    if (txtUserName.getText().equals(userAccount.getUsername())) {
                        JOptionPane.showMessageDialog(null, "UserName already Exist");
                        return;
                    }

                }
            }
            
            if (enterprise.getEnterpriseType().equals(Enterprise.EnterpriseType.Manufacturer)) {
                DrugManagerOrganization o = ((ManufacturerEnterprise) enterprise).getDrugManagerOrganization();

                for (UserAccount userAccount : o.getUserAccountDirectory().getUserAccountList()) {
                    if (txtUserName.getText().equals(userAccount.getUsername())) {
                        JOptionPane.showMessageDialog(null, "UserName already Exist");
                        return;
                    }

                }
            }
            
            if (enterprise.getEnterpriseType().equals(Enterprise.EnterpriseType.Manufacturer)) {
                PreClinicalTesterOrgnization o = ((ManufacturerEnterprise) enterprise).getPreClinicalTesterOrgnization();

                for (UserAccount userAccount : o.getUserAccountDirectory().getUserAccountList()) {
                    if (txtUserName.getText().equals(userAccount.getUsername())) {
                        JOptionPane.showMessageDialog(null, "UserName already Exist");
                        return;
                    }

                }
            }
            
            if (enterprise.getEnterpriseType().equals(Enterprise.EnterpriseType.Hospital)) {
                DispenserManagerOrganization o = ((HospitalEnterprise) enterprise).getDispenserManagerOrganization();

                for (UserAccount userAccount : o.getUserAccountDirectory().getUserAccountList()) {
                    if (txtUserName.getText().equals(userAccount.getUsername())) {
                        JOptionPane.showMessageDialog(null, "UserName already Exist");
                        return;
                    }

                }
            }
            
            if (enterprise.getEnterpriseType().equals(Enterprise.EnterpriseType.Hospital)) {
                DoctorOrganization o = ((HospitalEnterprise) enterprise).getDoctorOrganization();

                for (UserAccount userAccount : o.getUserAccountDirectory().getUserAccountList()) {
                    if (txtUserName.getText().equals(userAccount.getUsername())) {
                        JOptionPane.showMessageDialog(null, "UserName already Exist");
                        return;
                    }

                }
            }
            
            if (enterprise.getEnterpriseType().equals(Enterprise.EnterpriseType.License)) {
                LicenseManagerOrganization o = ((LicenseEnterprise) enterprise).getLicenseManagerOrganization();

                for (UserAccount userAccount : o.getUserAccountDirectory().getUserAccountList()) {
                    if (txtUserName.getText().equals(userAccount.getUsername())) {
                        JOptionPane.showMessageDialog(null, "UserName already Exist");
                        return;
                    }

                }
            }
            
            if (enterprise.getEnterpriseType().equals(Enterprise.EnterpriseType.Warehouse)) {
                WarehouseManagerOrganization o = ((WarehouseEnterprise) enterprise).getWarehouseManagerOrganization();

                for (UserAccount userAccount : o.getUserAccountDirectory().getUserAccountList()) {
                    if (txtUserName.getText().equals(userAccount.getUsername())) {
                        JOptionPane.showMessageDialog(null, "UserName already Exist");
                        return;
                    }

                }
            }
            
            if (enterprise.getEnterpriseType().equals(Enterprise.EnterpriseType.Warehouse)) {
                InventoryManagerOrganization o = ((WarehouseEnterprise) enterprise).getInventoryManagerOrganization();

                for (UserAccount userAccount : o.getUserAccountDirectory().getUserAccountList()) {
                    if (txtUserName.getText().equals(userAccount.getUsername())) {
                        JOptionPane.showMessageDialog(null, "UserName already Exist");
                        return;
                    }

                }
            }
        }
        
        
        if(cmbxEnterprise.getSelectedItem() instanceof FDAEnterprise)
        {
            for(Enterprise e : networkSelected.getEnterprisedirectory().getEnterpriseDirectory())
            {
                if(e.getEnterpriseType().getValue().equals("FDA"))
                {
                    Employee employee = e.getEmployeeDirectory().createEmployee(txtFirstName.getText(), txtLastName.getText());
                    UserAccount userAccount= e.getUserAccountDirectory().createUserAccount(txtUserName.getText(), txtPassword.getText(), employee, new FDAAdminRole());
                }
            }
        }

          if(cmbxEnterprise.getSelectedItem() instanceof ManufacturerEnterprise )
        {
            for(Enterprise e : networkSelected.getEnterprisedirectory().getEnterpriseDirectory())
            {
                if(e.getEnterpriseType().getValue().equals("Manufacturer"))
                {
                    Employee employee = e.getEmployeeDirectory().createEmployee(txtFirstName.getText(), txtLastName.getText());
                    UserAccount userAccount= e.getUserAccountDirectory().createUserAccount(txtUserName.getText(), txtPassword.getText(), employee, new ManufactureAdminRole());
                }
            }
        }
          
          if(cmbxEnterprise.getSelectedItem() instanceof HospitalEnterprise)
        {
            for(Enterprise e : networkSelected.getEnterprisedirectory().getEnterpriseDirectory())
            {
                if(e.getEnterpriseType().getValue().equals("Hospital"))
                {
                    Employee employee = e.getEmployeeDirectory().createEmployee(txtFirstName.getText(), txtLastName.getText());
                    UserAccount userAccount= e.getUserAccountDirectory().createUserAccount(txtUserName.getText(), txtPassword.getText(), employee, new HospitalAdminRole());
                }
            }
        }
          if(cmbxEnterprise.getSelectedItem() instanceof WarehouseEnterprise)
        {
            for(Enterprise e : networkSelected.getEnterprisedirectory().getEnterpriseDirectory())
            {
                if(e.getEnterpriseType().getValue().equals("Warehouse"))
                {
                    Employee employee = e.getEmployeeDirectory().createEmployee(txtFirstName.getText(), txtLastName.getText());
                    UserAccount userAccount= e.getUserAccountDirectory().createUserAccount(txtUserName.getText(), txtPassword.getText(), employee, new WarehouseAdminRole());
                }
            }
        }
          if(cmbxEnterprise.getSelectedItem() instanceof LicenseEnterprise)
        {
            for(Enterprise e : networkSelected.getEnterprisedirectory().getEnterpriseDirectory())
            {
                if(e.getEnterpriseType().getValue().equals("License"))
                {
                    Employee employee = e.getEmployeeDirectory().createEmployee(txtFirstName.getText(), txtLastName.getText());
                    UserAccount userAccount= e.getUserAccountDirectory().createUserAccount(txtUserName.getText(), txtPassword.getText(), employee, new LicenseAdminRole());
                }
            }
        }
//
        txtFirstName.setText("");
        txtLastName.setText("");
        txtUserName.setText("");
        txtPassword.setText("");
        

        populateTable();
        
    }//GEN-LAST:event_btnSubmitActionPerformed

