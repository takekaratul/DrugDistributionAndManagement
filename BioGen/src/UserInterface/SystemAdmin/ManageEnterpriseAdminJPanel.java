    private void populateTable() {
        DefaultTableModel model = (DefaultTableModel) enterpriseJTable.getModel();

        model.setRowCount(0);
        for (Network network : system.getNetworkList()) {
            for (Enterprise enterprise : network.getEnterprisedirectory().getEnterpriseDirectory()) {
                for (UserAccount userAccount : enterprise.getUserAccountDirectory().getUserAccountList()) {
                    Object[] row = new Object[3];
                    row[0] = enterprise.getOrganizationName();
                    row[1] = network.getName();
                    row[2] = userAccount.getUsername();

                    model.addRow(row);
                }
            }
        }
    }

    private void populateNetworkComboBox(){
        cmbxNetwork.removeAllItems();
        
        for (Network network : system.getNetworkList()){
            cmbxNetwork.addItem(network);
        }
    }
    
    private void populateEnterpriseComboBox(Network network){
        cmbxEnterprise.removeAllItems();
        
        for (Enterprise enterprise : network.getEnterprisedirectory().getEnterpriseDirectory()){
            cmbxEnterprise.addItem(enterprise);
        }
        
    }
    
